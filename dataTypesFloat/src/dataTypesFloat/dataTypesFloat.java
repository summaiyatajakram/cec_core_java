package dataTypesFloat;

public class dataTypesFloat {
	    public static void main(String[] args){
	        int val =9/2;
	        float val2 = 10f/6f;
	        double val3 = 10d/6d;
	        System.out.println("value1="+val);
	        System.out.println("value2="+val2);
	        System.out.println("value3="+val3);
	        int marker = 512;
	        double perc = marker*0.46f;
	        System.out.println("percentage="+perc);
	    }
	}


